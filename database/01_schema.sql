/* Create the tables */
CREATE TABLE IF NOT EXISTS provinces (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS districts (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL,
    province_id SMALLINT NOT NULL,
    FOREIGN KEY (province_id) REFERENCES provinces(id) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE IF NOT EXISTS wards (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL,
    district_id SMALLINT NOT NULL,
    FOREIGN KEY (district_id) REFERENCES districts(id) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE IF NOT EXISTS genders (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS users (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    full_name VARCHAR(255) NOT NULL,
    avatar_url VARCHAR(255),
    phone VARCHAR(16) UNIQUE NOT NULL,
    email VARCHAR(100) UNIQUE NOT NULL,
    password VARCHAR(255) NOT NULL,
    birthday DATE NOT NULL,
    gender_id SMALLINT,
    ward_id SMALLINT,
    specific_address VARCHAR(255),
    creation_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (gender_id) REFERENCES genders(id) ON DELETE SET NULL ON UPDATE CASCADE,
    FOREIGN KEY (ward_id) REFERENCES wards(id) ON DELETE SET NULL ON UPDATE CASCADE
);

CREATE TABLE IF NOT EXISTS roles (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS permissions (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(100) NOT NULL,
    description VARCHAR(255)
);

CREATE TABLE IF NOT EXISTS user_role (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    user_id INTEGER NOT NULL,
    role_id SMALLINT NOT NULL,
    FOREIGN KEY (user_id) REFERENCES users(id) ON UPDATE CASCADE ON DELETE CASCADE,
    FOREIGN KEY (role_id) REFERENCES roles(id) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS role_permission (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    role_id SMALLINT NOT NULL,
    permission_id SMALLINT NOT NULL,
    FOREIGN KEY (role_id) REFERENCES roles(id) ON UPDATE CASCADE ON DELETE CASCADE,
    FOREIGN KEY (permission_id) REFERENCES permissions(id) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS branch_status (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS branches (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL,
    ward_id SMALLINT,
    specific_address VARCHAR(255),
    status_id SMALLINT,
    FOREIGN KEY (ward_id) REFERENCES wards(id) ON UPDATE CASCADE ON DELETE SET NULL,
    FOREIGN KEY (status_id) REFERENCES branch_status(id) ON UPDATE CASCADE ON DELETE SET NULL
);

CREATE TABLE IF NOT EXISTS branch_images (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    branch_id SMALLINT NOT NULL,
    image_link VARCHAR(255) NOT NULL,
    FOREIGN KEY (branch_id) REFERENCES branches(id) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS room_types (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL,
    icon_url VARCHAR(255),
    slogan VARCHAR(255),
    thumbnail_url VARCHAR(255),
    overview TEXT,
    extra_fee DECIMAL(10, 2) NOT NULL
);

CREATE TABLE IF NOT EXISTS room_type_features (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    room_type_id SMALLINT NOT NULL,
    image_url VARCHAR(255),
    title VARCHAR(255),
    description TEXT,
    FOREIGN KEY (room_type_id) REFERENCES room_types(id) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS room_status (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS rooms (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    branch_id SMALLINT NOT NULL,
    name VARCHAR(255) NOT NULL,
    row_count SMALLINT NOT NULL,
    column_count SMALLINT NOT NULL,
    room_type_id SMALLINT NOT NULL,
    status_id SMALLINT NOT NULL,
    FOREIGN KEY (branch_id) REFERENCES branches(id) ON UPDATE CASCADE ON DELETE CASCADE,
    FOREIGN KEY (room_type_id) REFERENCES room_types(id) ON UPDATE CASCADE,
    FOREIGN KEY (status_id) REFERENCES room_status(id) ON UPDATE CASCADE
);

CREATE TABLE IF NOT EXISTS seat_types (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL,
    extra_fee DECIMAL(10, 2) NOT NULL
);

CREATE TABLE IF NOT EXISTS seat_status (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS seats (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    room_id INTEGER NOT NULL,
    name VARCHAR(5) NOT NULL,
    x_position SMALLINT NOT NULL,
    y_position SMALLINT NOT NULL,
    seat_type_id SMALLINT NOT NULL,
    status_id SMALLINT NOT NULL,
    FOREIGN KEY (room_id) REFERENCES rooms(id) ON UPDATE CASCADE,
    FOREIGN KEY (seat_type_id) REFERENCES seat_types(id) ON UPDATE CASCADE,
    FOREIGN KEY (status_id) REFERENCES seat_status(id) ON UPDATE CASCADE
);

CREATE TABLE IF NOT EXISTS products (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL,
    thumbnail_url VARCHAR(255),
    price DECIMAL(10, 2) NOT NULL
);

CREATE TABLE IF NOT EXISTS product_status (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS product_branch (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    branch_id SMALLINT NOT NULL,
    product_id SMALLINT NOT NULL,
    status_id SMALLINT NOT NULL,
    FOREIGN KEY (branch_id) REFERENCES branches(id) ON UPDATE CASCADE ON DELETE CASCADE,
    FOREIGN KEY (product_id) REFERENCES products(id) ON UPDATE CASCADE ON DELETE CASCADE,
    FOREIGN KEY (status_id) REFERENCES product_status(id) ON UPDATE CASCADE
);

CREATE TABLE IF NOT EXISTS languages (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS age_ratings (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    code VARCHAR(5) NOT NULL UNIQUE,
    description VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS movies (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    title VARCHAR(255) NOT NULL,
    poster_url VARCHAR(255),
    backdrop_url VARCHAR(255),
    trailer_url VARCHAR(255),
    tagline VARCHAR(255),
    overview TEXT,
    released_date DATE NOT NULL,
    duration SMALLINT NOT NULL,
    original_language_id SMALLINT NOT NULL,
    age_rating_id SMALLINT,
    FOREIGN KEY (original_language_id) REFERENCES languages(id) ON UPDATE CASCADE,
    FOREIGN KEY (age_rating_id) REFERENCES age_ratings(id) ON UPDATE CASCADE
);

CREATE TABLE IF NOT EXISTS movie_images (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    movie_id INTEGER NOT NULL,
    image_url VARCHAR(255) NOT NULL,
    FOREIGN KEY (movie_id) REFERENCES movies(id) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS keywords (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS movie_keyword(
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    movie_id INTEGER NOT NULL,
    keyword_id INTEGER NOT NULL,
    FOREIGN KEY (movie_id) REFERENCES movies(id) ON UPDATE CASCADE ON DELETE CASCADE,
    FOREIGN KEY (keyword_id) REFERENCES keywords(id) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS movie_reviews (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    user_id INTEGER NOT NULL,
    movie_id INTEGER NOT NULL,
    rating SMALLINT NOT NULL,
    comment TEXT,
    creation_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES users(id) ON UPDATE CASCADE ON DELETE CASCADE,
    FOREIGN KEY (movie_id) REFERENCES movies(id) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS movie_scores (
    movie_id INTEGER PRIMARY KEY,
    rating_score DOUBLE PRECISION NOT NULL,
    view_score DOUBLE PRECISION NOT NULL,
    ticket_score DOUBLE PRECISION NOT NULL,
    showtime_score DOUBLE PRECISION NOT NULL,
    final_score REAL NOT NULL,
    last_updated TIMESTAMP,
    FOREIGN KEY (movie_id) REFERENCES movies(id) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS movie_view_history (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    user_id INTEGER NOT NULL,
    movie_id INTEGER NOT NULL,
    start_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    end_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (user_id) REFERENCES users(id) ON UPDATE CASCADE ON DELETE CASCADE,
    FOREIGN KEY (movie_id) REFERENCES movies(id) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS people (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    gender_id SMALLINT NOT NULL,
    name VARCHAR(255) NOT NULL,
    avatar_url VARCHAR(255),
    FOREIGN KEY (gender_id) REFERENCES genders(id) ON UPDATE CASCADE
);

CREATE TABLE IF NOT EXISTS departments (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS positions (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    department_id SMALLINT NOT NULL,
    name VARCHAR(255) NOT NULL,
    FOREIGN KEY (department_id) REFERENCES departments(id) ON UPDATE CASCADE
);

CREATE TABLE IF NOT EXISTS movie_cast (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    movie_id INTEGER NOT NULL,
    person_id INTEGER NOT NULL,
    character VARCHAR(512),
    cast_order SMALLINT NOT NULL,
    FOREIGN KEY (movie_id) REFERENCES movies(id) ON UPDATE CASCADE ON DELETE CASCADE,
    FOREIGN KEY (person_id) REFERENCES people(id) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS movie_crew (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    movie_id INTEGER NOT NULL,
    person_id INTEGER NOT NULL,
    position_id INTEGER NOT NULL,
    FOREIGN KEY (position_id) REFERENCES positions(id) ON UPDATE CASCADE,
    FOREIGN KEY (movie_id) REFERENCES movies(id) ON UPDATE CASCADE ON DELETE CASCADE,
    FOREIGN KEY (person_id) REFERENCES people(id) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS categories (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS movie_category (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    movie_id INTEGER NOT NULL,
    category_id SMALLINT NOT NULL,
    FOREIGN KEY (movie_id) REFERENCES movies(id) ON UPDATE CASCADE ON DELETE CASCADE,
    FOREIGN KEY (category_id) REFERENCES categories(id) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS showtime_status (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS showtimes (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    movie_id INTEGER NOT NULL,
    room_id INTEGER NOT NULL,
    start_time TIMESTAMP NOT NULL,
    status_id SMALLINT NOT NULL,
    FOREIGN KEY (movie_id) REFERENCES movies(id) ON UPDATE CASCADE,
    FOREIGN KEY (room_id) REFERENCES rooms(id) ON UPDATE CASCADE,
    FOREIGN KEY (status_id) REFERENCES showtime_status(id) ON UPDATE CASCADE
);

CREATE TABLE IF NOT EXISTS reservation_status (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS reservations (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    showtime_id INTEGER NOT NULL,
    user_id INTEGER NOT NULL,
    start_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    end_time TIMESTAMP NOT NULL,
    status_id SMALLINT NOT NULL,
    FOREIGN KEY (showtime_id) REFERENCES showtimes(id) ON UPDATE CASCADE,
    FOREIGN KEY (user_id) REFERENCES users(id) ON UPDATE CASCADE,
    FOREIGN KEY (status_id) REFERENCES reservation_status(id) ON UPDATE CASCADE
);

CREATE TABLE IF NOT EXISTS reservation_seat (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    reservation_id INTEGER NOT NULL,
    seat_id INTEGER NOT NULL,
    FOREIGN KEY (reservation_id) REFERENCES reservations(id) ON UPDATE CASCADE,
    FOREIGN KEY (seat_id) REFERENCES seats(id) ON UPDATE CASCADE
);

CREATE TABLE IF NOT EXISTS reservation_product (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    reservation_id INTEGER NOT NULL,
    product_id SMALLINT NOT NULL,
    quantity SMALLINT NOT NULL,
    FOREIGN KEY (reservation_id) REFERENCES reservations(id) ON UPDATE CASCADE,
    FOREIGN KEY (product_id) REFERENCES products(id) ON UPDATE CASCADE
);

CREATE TABLE IF NOT EXISTS ticket_status (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS tickets (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    reservation_id INTEGER NOT NULL,
    status_id SMALLINT NOT NULL,
    FOREIGN KEY (reservation_id) REFERENCES reservations(id) ON UPDATE CASCADE,
    FOREIGN KEY (status_id) REFERENCES ticket_status(id) ON UPDATE CASCADE
);

CREATE TABLE IF NOT EXISTS ticket_price (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    day_of_week SMALLINT NOT NULL,
    time_range_start TIME NOT NULL,
    time_range_end TIME NOT NULL,
    price DECIMAL(10, 2) NOT NULL
);

CREATE TABLE IF NOT EXISTS age_discounts (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    age_min SMALLINT,
    age_max SMALLINT,
    discount_percent DECIMAL(5, 2) NOT NULL
);

CREATE TABLE IF NOT EXISTS payment_method_status (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS payment_methods (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL,
    thumbnail_url VARCHAR(255),
    status_id SMALLINT NOT NULL,
    FOREIGN KEY (status_id) REFERENCES payment_method_status(id) ON UPDATE CASCADE
);

CREATE TABLE IF NOT EXISTS payment_transaction_status (
    id SMALLINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS payment_transactions (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    reservation_id INTEGER NOT NULL,
    payment_method_id SMALLINT NOT NULL,
    transaction_id VARCHAR(255) NOT NULL,
    time TIMESTAMP NOT NULL,
    amount DECIMAL(10, 2) NOT NULL,
    status_id SMALLINT NOT NULL,
    FOREIGN KEY (reservation_id) REFERENCES reservations(id) ON UPDATE CASCADE,
    FOREIGN KEY (payment_method_id) REFERENCES payment_methods(id) ON UPDATE CASCADE,
    FOREIGN KEY (status_id) REFERENCES payment_transaction_status(id) ON UPDATE CASCADE
);

CREATE TABLE IF NOT EXISTS refresh_tokens (
    id VARCHAR(36) PRIMARY KEY,
    user_id INTEGER NOT NULL,
    device_info VARCHAR(255),
    expiration_time TIMESTAMP NOT NULL,
    FOREIGN KEY (user_id) REFERENCES users(id)
);